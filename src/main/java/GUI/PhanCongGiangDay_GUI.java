   public void TableOutPut(Vector<CourseInstructor_DTO> l){
        model= (DefaultTableModel) phancong_tb.getModel();
        model.setRowCount(0);
        for(CourseInstructor_DTO obj : l){
            
            courseID=obj.getCourse().getId();
            courseName=obj.getCourse().getTitle();
            PersonID=obj.getPerson().getId();
            PersonName=obj.getPerson().getLastName()+" "+obj.getPerson().getFirstName();
            deparment=obj.getCourse().getDepartment().getName();
            if (PersonID==-1)
            {
             Object[] row={courseID, courseName, "", PersonName, deparment};
            model.addRow(row);
            }
            else
            {
            Object[] row={courseID, courseName, PersonID, PersonName, deparment};
             model.addRow(row);
            }
            
           
                }
        }
    
