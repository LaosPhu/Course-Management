    public void checkFilterCourseInstructor()
    {
        String temp = search_tf.getText().toString().trim();
        resetThem();
        try {
            
            if(!temp.isEmpty() && search_select_cb.getSelectedIndex()==0){
                listCInt=new Vector<>(couInsBLL.All_Search("course.CourseID LIKE '%"+temp+"%'"));
                TableOutPut(listCInt);
            }else if(!temp.isEmpty() && search_select_cb.getSelectedIndex()==1){
                listCInt=new Vector<>(couInsBLL.All_Search("course.Title LIKE N'%"+temp+"%'"));
                TableOutPut(listCInt);
            }else if(!temp.isEmpty() && search_select_cb.getSelectedIndex()==2){
                listCInt=new Vector<>(couInsBLL.All_Search("person.PersonID LIKE '%"+temp+"%'"));
                TableOutPut(listCInt);
            }else if(!temp.isEmpty() && search_select_cb.getSelectedIndex()==3){
                listCInt=new Vector<>(couInsBLL.All_Search("CONCAT(person.Lastname, ' ', person.Firstname) LIKE N'%"+temp+"%'"));
                TableOutPut(listCInt);
            }else if(!temp.isEmpty() && search_select_cb.getSelectedIndex()==4){
                listCInt=new Vector<>(couInsBLL.All_Search("department.name LIKE N'%"+temp+"%'"));
                TableOutPut(listCInt);
            }else if(temp.isEmpty()){
                listCInt=new Vector<>(couInsBLL.readCourseInstructor());
                TableOutPut(listCInt);
            }
            
             if (all_btn.isSelected()==true)
            {
                 filter_CourseInstructor(couInsBLL.getCourseListInstructor());
            }
            else if (notIns_btn.isSelected()==true)
            {
                filter_CourseInstructor(couInsBLL.CourseNInstructor());
            }
            else
            {
                 filter_CourseInstructor(couInsBLL.CourseInstructor());
            }
        } catch (SQLException ex) {
            System.out.println("GUI"+ex);
        }
       
    }
