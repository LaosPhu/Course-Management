    private boolean checkBeforeAdd() {
        boolean check = true;
        if(jtfTitle.getText().isEmpty()) {
            check = false;
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên khóa học", "Lỗi", JOptionPane.WARNING_MESSAGE);
        }
        if (jcbOnlineOnsite.getSelectedIndex() < 0) {
            check = false;
            JOptionPane.showMessageDialog(null, "Vui lòng chọn loại khóa học", "Lỗi", JOptionPane.WARNING_MESSAGE);
        }
        if(jcbOnlineOnsite.getSelectedItem() == "Online"){
            if(jtfUrl.getText() == "") {
                check = false;
                JOptionPane.showMessageDialog(null, "Vui lòng nhập URL khóa học", "Lỗi", JOptionPane.WARNING_MESSAGE);
            }
        }
        if(jcbOnlineOnsite.getSelectedItem() == "Onsite"){
            if(jtfLocation.getText() == "") {
                check = false;
                JOptionPane.showMessageDialog(null, "Vui lòng nhập địa chỉ khóa học", "Lỗi", JOptionPane.WARNING_MESSAGE);
            }
            boolean checked = false;
            for (JPanel panel : checkBoxPanels) {
                JCheckBox checkBox = (JCheckBox) panel.getComponent(0);
                    if (checkBox.isSelected()) {
                        checked = true;
                    }
            }
            if(!checked) {
                check = false;
                JOptionPane.showMessageDialog(null, "Vui lòng chọn ít nhất một ngày học", "Lỗi", JOptionPane.WARNING_MESSAGE);
            }
        }
        if(jcbDepartment.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn một khoa", "Lỗi", JOptionPane.WARNING_MESSAGE);
            check = false;
        }
        return check;
    }
    
    private void jbtnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnThemActionPerformed
        if(!checkBeforeAdd()) {
            return;
        }
        int CourseID = Integer.parseInt(jtfCourseId.getText());
        Department dpm = (Department)jcbDepartment.getSelectedItem();
        String name = jtfTitle.getText();
        int Credits = (int) jspCredits.getValue();
        Course c = new Course();
        c.setCourseId(CourseID);
        c.setCredits(Credits);
        c.setTitle(name);
        if(dpm != null) {
            c.setDepartmentId(dpm.getDepartmentID());
        }
        else {
             c.setDepartmentId(0);
        }
        if(jcbOnlineOnsite.getSelectedItem() == "Online") {
            try {
                String url = jtfUrl.getText();
                OnlineCourse olc = new OnlineCourse();
                olc.setUrl(url);
                olc.setCourseId(CourseID);
                if(couBLL.addCourse(c) > 0 && olcBLL.addOnlineCourse(olc) > 0) {
                    JOptionPane.showConfirmDialog(null, "Thêm khóa online thành công!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddNewCourse.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        else if (jcbOnlineOnsite.getSelectedItem() == "Onsite") {
            try {
                String location = jtfLocation.getText();
                String Days = "";
                for (JPanel panel : checkBoxPanels) {
                    JCheckBox checkBox = (JCheckBox) panel.getComponent(0);
                    if (checkBox.isSelected()) {
                        if(checkBox.getText() == "Thu") {
                             Days += "H";
                        } 
                        else {
                            Days += checkBox.getText().charAt(0);
                        }
                        
                    }
                }
                Time time = Time.valueOf("00:00:00");
                time.setHours((int)hourSpinner.getValue());
                time.setMinutes((int)minuteSpinner.getValue());
                OnsiteCourse osc = new OnsiteCourse();
                osc.setCourseID(CourseID);
                osc.setDays(Days);
                osc.setLocation(location);
                osc.setTime(time);
                if(couBLL.addCourse(c) > 0 && oscBLL.addOnsiteCourse(osc) > 0) {
                    JOptionPane.showConfirmDialog(null, "Thêm khóa onsite thành công!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddNewCourse.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jbtnThemActionPerformed

