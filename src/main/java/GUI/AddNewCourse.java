     private void textFieldChange() {
        jtfTitle.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                textChanged();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                textChanged();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                textChanged();
            }

            private void textChanged() {
                String tf = jtfTitle.getText();
                String tfid = jtfCourseId.getText();
                if(tf.isEmpty()) {
                    jtfCourseId.setText("");
                }
                else if(tfid.isEmpty()){
                    try {
                        int id = couBLL.generateCourseID();
                        jtfCourseId.setText(id+"");
                    } catch (SQLException ex) {
                        Logger.getLogger(AddNewCourse.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });
    }
    
